'use strict'

/*

	Funciones en Javascript

	Una funcion es un conjunto de Instrucciones que pueden ser llamadas las veces que queramos.

*/

function Operaciones(n1,n2){
	document.write("<p>Numero 1: <b>"+n1+"</b>, Numero 2: <b>"+n2+"</b></p>")
	document.write("<p>La suma es: "+(n1+n2)+"</p>");
	document.write("<p>La resta es: "+(n1-n2)+"</p>");
	document.write("<p>La multiplicación es: "+(n1*n2)+"</p>");
	document.write("<p>La división es: "+(n1/n2)+"</p>");
	document.write("************************************************")
}

Operaciones(7,3);
Operaciones(5,10);
