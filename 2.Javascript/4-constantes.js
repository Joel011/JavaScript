"use strict"

//Constantes en Javascript
//Las constantes se usan para asignar valores que jamas podran cambiar a lo largo de nuestro programa
//Para declarar una constante se utiliza la palabra reservada const

var nombre="Joel"; //Variable : Puede cambiar 
const edad = 20; //Constante: No puede cambiar

console.log("Nombre: "+nombre);
console.log("Edad: "+edad);
